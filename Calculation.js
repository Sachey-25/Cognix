exports.add = function (number_one,number_two){
    return number_one+number_two;
};
exports.sub = function (number_one,number_two){
    return number_one-number_two;
};
exports.mul = function (number_one,number_two){
    return number_one*number_two;
};
exports.div = function (number_one,number_two){
    return number_one/number_two;
};